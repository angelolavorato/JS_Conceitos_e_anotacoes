let number = 1

console.log(number + 1) //binary
console.log(--number) //unário (decremento)
console.log(++number) //unário (incrementação)
console.log(typeof number) //unário (operador, apresenta o tipo)
console.log(true ? 'alo' : 'nada') //ternário(quando se tem 3 expressões)
